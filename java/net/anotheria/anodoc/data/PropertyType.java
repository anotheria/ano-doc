package net.anotheria.anodoc.data;
/**
 * Declaration of supported types for properties.
 * @author lrosenberg
 *
 */
public enum PropertyType {
	/**
	 * Integer.
	 */
	INT,
	/**
	 * Long.
	 */
	LONG,
	/**
	 * Double.
	 */
	DOUBLE,
	/**
	 * Float.
	 */
	FLOAT,
	/**
	 * List of properties.
	 */
	LIST,
	/**
	 * String.
	 */
	STRING,
	/**
	 * Text. Same as String but with different editors.
	 */
	TEXT,
	/*
	 * Boolean.
	 */
	BOOLEAN
}
